import { Box, Button, ButtonProps, Center, Text, Textarea, useBreakpointValue } from '@chakra-ui/react';
import Head from 'next/head';
import { useState, ChangeEventHandler } from 'react';

import { AppColors, HOVERABLE_CLASS } from '../constant';

// ********************************************************************************
// == Constant ====================================================================
const buttonProps: Partial<ButtonProps> = {
  className: HOVERABLE_CLASS,
  color: AppColors.WHITE,
  backgroundColor: AppColors.PURPLE,
  _active: { color: AppColors.PURPLE, backgroundColor: AppColors.WHITE_2 },
  _hover: { color: AppColors.PURPLE, backgroundColor: AppColors.WHITE },
}

// == Component ===================================================================
const MainPage = () => {
  // -- State ---------------------------------------------------------------------
  const [textAreaValue, setTextAreaValue] = useState('')

  // -- Handler -------------------------------------------------------------------
  const handleTextAreaChange: ChangeEventHandler<HTMLTextAreaElement> = (event) =>
    setTextAreaValue(event.target.value)

  // -- UI ------------------------------------------------------------------------
  return (
    <>
      <Head>
        <meta name='description' content='Generated by create next app' />
        <meta name='viewport' content='width=device-width, initial-scale=1' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      <Box
        position='relative'
        width='100vw'
        height='100vh'
        flexDir='column'
        justifyContent='space-between'
        overflowX='auto'
        overflowY='auto'
        color={AppColors.WHITE}
        backgroundColor={AppColors.BLACK_1}
      >
        <Box padding='4em'>
          <Text
            padding='0.25em'
            backgroundColor={AppColors.PURPLE}
            borderRadius='16px'
            width='fit-content'
            fontSize={useBreakpointValue({ base: '1.5em', md: '2.5em' })}
            fontWeight='bold'
          >
            Clipboard Injector
          </Text>

          <Center padding='2em' gap='5em'>
            <Button {...buttonProps}>
              Copy as Text
            </Button>
            <Button {...buttonProps}>
              Copy as HTML
            </Button>
          </Center>

          <Textarea value={textAreaValue} onChange={handleTextAreaChange} />

        </Box>
      </Box>
    </>
  )
}

export default MainPage;